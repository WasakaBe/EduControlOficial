CREATE TABLE TBL_USERS (
    ID_USERS INT AUTO_INCREMENT PRIMARY KEY,
    NAME_USERS VARCHAR(255) NOT NULL,
    APP_USERS VARCHAR(255) NOT NULL,
    APM_USERS VARCHAR(255),
    AGE_USERS INT,
    FECHA_NACIMIENTO_USERS DATE,
    CORREO_USERS VARCHAR(255) UNIQUE NOT NULL,
    PWD_USERS VARCHAR(255) NOT NULL,
    FOTO_USERS LONGBLOB,
    NO_CONTROL_USERS BIGINT UNSIGNED UNIQUE,
    PHONE_USERS BIGINT UNSIGNED,
    SEGURO_SOCIAL_USERS BIGINT UNSIGNED,
    CURP_USERS VARCHAR(18) UNIQUE,
    IDRol INT,
    IDSexo INT,
    IDParental INT,
    FOREIGN KEY (IDRol) REFERENCES TBL_ROLS_USER(ID_ROLS_USER),
    FOREIGN KEY (IDSexo) REFERENCES TBL_SEXS(ID_SEXS),
    FOREIGN KEY (IDParental) REFERENCES TBL_PARENTAL(ID_PARENTAL)
);
